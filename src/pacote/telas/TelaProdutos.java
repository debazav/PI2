    private void bttPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bttPesquisarActionPerformed
        boolean resultSearch = false;
        ultimaPesquisa = txtDescricaoPesquisarProdutos.getText();
        
        try{
            resultSearch = refreshList();
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Falha ao obter lista");
            return;
        }
        
        if(!resultSearch){
            JOptionPane.showMessageDialog(null, "Sem resultados");
        }
        
    }//GEN-LAST:event_bttPesquisarActionPerformed
            
    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if(tabelaProduto.getSelectedRow() >= 0){
            final int row = tabelaProduto.getSelectedRow();
            String desc = (String) tabelaProduto.getValueAt(row, 0);
            int resposta = JOptionPane.showConfirmDialog(null, "Excluir Produto\n"+desc+"?");
            if(resposta==0){
                try{
                    this.refreshList();
                }catch(Exception e){
                    JOptionPane.showMessageDialog(null, "Falha na exclusão");
                }
            }
            
        }
    }//GEN-LAST:event_jButton3ActionPerformed
    
    String ultimaPesquisa = null;
    
    public boolean refreshList(){
        List<Produto> resultado = ServicoProduto.procurarProduto(ultimaPesquisa);
        
        DefaultTableModel model = (DefaultTableModel) tabelaProduto.getModel();
        model.setRowCount(0);
        
        if(resultado==null || resultado.size() <=0){
            return false;
        }
        
        for(int i = 0; i < resultado.size(); i++){
            Produto produto = resultado.get(i);
            if(produto!=null){
                Object[] row = new Object[6];
                row[0] = produto.getDesc();
                row[1] = produto.getCat();
                row[2] = produto.getMarca();
                row[3] = produto.getModelo();
                row[4] = produto.getCor();
                row[5] = produto.getQtde();
                model.addRow(row);
            }
        }
        return true;
    }

