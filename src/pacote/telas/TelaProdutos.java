    private void bttSalvarCadastroProdutoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bttSalvarCadastroProdutoActionPerformed
        Produto produto = new Produto();
        produto.setDesc(txtDescricaoCadastrarProdutos.getText());
        String categoria = (String) jComboBoxCategoria.getSelectedItem();
        produto.setMarca(txtMarca.getText());
        produto.setModelo(txtModelo.getText());
        produto.setCor(txtCor.getText());
        produto.setQtde(txtQuantidade.getText());
        produto.setCat(categoria);
        ServicoProduto.cadastrarProduto(produto);
        
        if(ValidadorProduto.validador(produto)==0){
            txtDescricaoCadastrarProdutos.setText("");
            jComboBoxCategoria.setSelectedIndex(0);
            txtMarca.setText("");
            txtModelo.setText("");
            txtCor.setText("");
            txtQuantidade.setText("");
            
        }
        
        
        
    }//GEN-LAST:event_bttSalvarCadastroProdutoActionPerformed

